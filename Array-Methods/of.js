"use strict";
//Array.of()
// creamos un array de un elemento
let numerosarray = Array.of(3);
console.log(numerosarray); // [ 3 ]
// ccreamos array de 3 elementos string
let fruits = Array.of("Apple", "Banana", "Grapes");
console.log(fruits); // [ 'Apple', 'Banana', 'Grapes' ]
// creamos array con  4 integeros 
let primeNumbers = Array.of(2, 3, 5, 7);
console.log(primeNumbers); // [ 2, 3, 5, 7 ]